        String tanggal_produksi = jTable1.getValueAt(baris, 4).toString();
        String harga_pupuk = jTable1.getValueAt(baris, 5).toString();
        jTextField1.setText(id_pupuk);
        jTextField2.setText(Nama_pupuk);

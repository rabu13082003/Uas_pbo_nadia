 public void tampilDataBooking(){
    try {
            modelTabelBooking.getDataVector().removeAllElements();
            modelTabelBooking.fireTableDataChanged();
        
            PreparedStatement query = myObject.Konek.prepareStatement("SELECT * FROM pupuk");
            ResultSet data = query.executeQuery();
            while (data.next()) {
            Object[] baris = new Object[6];
            baris[0] = data.getString("id_pupuk");
            baris[1] = data.getString("Nama_pupuk");
            baris[2] = data.getString("jenis_pupuk");
            baris[3] = data.getString("jumlah_stok");
            baris[4] = data.getString("tanggal_produksi");
            baris[5] = data.getString("harga_pupuk");
            modelTabelBooking.addRow(baris);
            
            }
            query.close();
            data.close();
            } catch (Exception e){
            }
        
    
    }
